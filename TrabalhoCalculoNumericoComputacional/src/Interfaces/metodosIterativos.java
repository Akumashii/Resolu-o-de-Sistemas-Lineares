    public void GeraMatrizA(){
        int n = Integer.parseInt(txtTamanho.getText());

        DefaultTableModel matrizA = new DefaultTableModel(n, n+1) {
            @Override
            public Class<?> getColumnClass(int columnIndex) {
                if (columnIndex == 0) return String.class; // Coluna de rótulos
                return Double.class; // Colunas de valores numéricos
            }
            @Override
            public boolean isCellEditable(int row, int column) {
                return column != 0;
            }
        };

        // Aplica o novo modelo à sua tabela
        tabelaMatrizA.setModel(matrizA);

        //define nome Colunas
        for(int i = 0; i < n+1; i++) {
            if(i == 0) {
                tabelaMatrizA.getColumnModel().getColumn(i).setHeaderValue("Matriz");
            } else {
                tabelaMatrizA.getColumnModel().getColumn(i).setHeaderValue("Coluna: " + i);
            }
        }
        //define nome Linhas
        for(int i = 0; i < n; i++) {
            // Definimos o valor da célula na Linha 'i', Coluna 0
            tabelaMatrizA.setValueAt("Linha " + (i + 1), i, 0);
        }       

        AjusteTabela(tabelaMatrizA);
        tabelaMatrizA.getTableHeader().repaint();
    }

