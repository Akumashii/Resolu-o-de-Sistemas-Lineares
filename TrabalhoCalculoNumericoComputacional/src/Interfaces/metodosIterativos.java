    public void PreencherVetorResultado(javax.swing.JTable tabela, double[] resultados) {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();

        if (model.getRowCount() < resultados.length) {
            model.setRowCount(resultados.length);
        }

        for (int i = 0; i < resultados.length; i++) {
            //setValueAt(valor, linha, coluna)
            model.setValueAt(resultados[i], i, 1);
        }

        tabela.revalidate();  
        tabela.repaint();   
    }
    
    public void PreencherMatrizResultado(javax.swing.JTable tabela, double[][] resultados) {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();

        if (model.getRowCount() < resultados.length) {
            model.setRowCount(resultados.length);
        }

        for (int i = 0; i < resultados.length; i++) {
            for(int j = 0; j< resultados.length; j++){
                model.setValueAt(resultados[i][j], i, j+1);
            }
        }

        tabela.revalidate();  
        tabela.repaint();   
    }
    
    public void PreencherResultado(javax.swing.JTable tabela, double[] resultados) {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();

        model.setRowCount(0); 

        Object[] linha = new Object[resultados.length];
        for (int i = 0; i < resultados.length; i++) {
            linha[i] = resultados[i];
        }

        model.addRow(linha);

        tabela.revalidate();  
        tabela.repaint();   
    }
    
    public double[][] ArmazenaMatrizA(javax.swing.JTable tabela) {
        int n = tabela.getRowCount();
        
        double[][] matrizA = new double[n][n];

        for (int i = 0; i < n; i++) {
            //j em 1 para pular a coluna de texto
            for (int j = 1; j < tabela.getColumnCount(); j++) {
                try {
                    Object valor = tabela.getValueAt(i, j);
                    
                    if (valor == null || valor.toString().isEmpty()) {
                        matrizA[i][j - 1] = 0.0;
                    } else {                       
                        matrizA[i][j - 1] = Double.parseDouble(valor.toString());
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Erro na linha " + (i+1) + ": valor inválido.");
                    return null; 
                }
            }
        }
        return matrizA;
    }
    
