    DefaultTableCellRenderer renderizador = new DefaultTableCellRenderer() {
    @Override
    public java.awt.Component getTableCellRendererComponent(javax.swing.JTable table, Object value, 
            boolean isSelected, boolean hasFocus, int row, int column) {
        
        // Deixamos o Java criar o componente padrão primeiro
        java.awt.Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

        
        if (column == 0 ) {
            c.setBackground(new java.awt.Color(230, 230, 230)); // Um cinza claro
            c.setForeground(java.awt.Color.BLACK);            // Cor do texto
            c.setFont(c.getFont().deriveFont(java.awt.Font.BOLD)); // Texto em negrito
        } else {
            
            c.setBackground(java.awt.Color.WHITE);
            c.setForeground(java.awt.Color.BLACK);
        }
 
        if (isSelected) {
            c.setBackground(table.getSelectionBackground());
        }

        return c;
        }
    };

    public void AjusteTabela(javax.swing.JTable table){
        //ajuste de tamanho da cedula
        for (int i = 0; i < table.getColumnCount(); i++) {
                table.getColumnModel().getColumn(i).setPreferredWidth(60);
       }
       //pintar a cedulas não editaveis
        for (int i = 0; i < table.getColumnCount(); i++) {
            table.getColumnModel().getColumn(i).setCellRenderer(renderizador);
        }
    }
    
